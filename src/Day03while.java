public class Day03while {
    public static void main(String[] args)
    {
        /*
        定义初始化表达式；
        while（条件表达式）；
        {
        循环体（执行语句）；
        }
         */
            int x = 5;
            while (x<=20)
            {
                System.out.println("x="+x);
                x++;
            }
            {
        }
    }
}
