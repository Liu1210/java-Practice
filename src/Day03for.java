public class Day03for {
    public static void main(String[] args){
        /*
        for（初始化表達式；循环条件表达式；循环后的操作表达式；）
        {
        执行语句
        }；
         */
        for(int x = 0;x<6;x++)
        {
            System.out.print("for x ="+x);
            System.out.print("\n");
        }
    }
}
